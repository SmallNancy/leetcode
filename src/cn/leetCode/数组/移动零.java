/**
 * @Author: Nancy
 * @Date: 2019/4/12 14:16
 * 给定一个数组 nums，编写一个函数将所有 0 移动到数组的末尾，同时保持非零元素的相对顺序。
 */
public class 移动零 {
    public static void main(String[] args) {
        int[] arr = {1,0,0,2};
        moveZeroes(arr);
        for (int i:arr){
            System.out.print(i + " ");
        }
    }
    public static void moveZeroes(int[] nums) {
        int temp = 0;
        for (int i =0;i < nums.length; i++) {
           if (nums[i] != 0) {
               nums[temp++] = nums[i];
           }
        }
        for (int i = temp;i < nums.length;i++){
            nums[temp++] =0;
        }


    }
}
